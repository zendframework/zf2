    {
        return static::$types;
    }

    /**
     * Get Countries
     *
     * @param  string $type
     * @return array[string]
     */
    public static function getCountries($type = 'official')
    {
        if (!in_array($type, static::$types)) {
            throw new Exception\InvalidArgumentException('Unknown countries type: ' . $type);
        }

        return static::$$type;
    }
